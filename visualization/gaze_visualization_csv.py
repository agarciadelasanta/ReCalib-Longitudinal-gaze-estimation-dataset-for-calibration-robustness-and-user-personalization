def plot_scatter_with_grid(df_plot, x_bins, y_bins, x_value, y_value, n_regions, percentages, title, xlim=None, ylim=None, show_screen=False, screen_w=260, screen_h=170, cam_y=10):
    x = pd.to_numeric(df_plot[x_value], errors="coerce")
    y = pd.to_numeric(df_plot[y_value], errors="coerce")
    
    fig, ax = plt.subplots(figsize=(8, 6))

    if show_screen:
        rect = Rectangle((-screen_w / 2.0, cam_y), screen_w, screen_h, edgecolor='black', facecolor='none', linewidth=2, linestyle='-')
        ax.add_patch(rect)
        
    if 'task_type' in df_plot.columns:
        color_map = {'test': 'orange', 'calibration': 'blue'}
        unique_tasks = df_plot['task_type'].dropna().unique()
        ordered_tasks = [t for t in ['test', 'calibration'] if t in unique_tasks]
        ordered_tasks += [t for t in unique_tasks if t not in ['test', 'calibration']]
        
        for t_type in ordered_tasks:
            m = (df_plot['task_type'] == t_type) & x.notna() & y.notna()
            if m.sum() > 0:
                c = color_map.get(t_type, 'gray')
                ax.scatter(x[m], y[m], alpha=0.5, s=8, color=c, label=t_type.capitalize())
        ax.legend(loc='upper right')
    else:
        m = x.notna() & y.notna()
        ax.scatter(x[m], y[m], alpha=0.5, s=8, label="All Data")
        ax.legend(loc='upper right')

    ax.set_xlabel(x_value)
    ax.set_ylabel(y_value)
    ax.set_title(title)

    if xlim is not None: ax.set_xlim(xlim[0], xlim[1])
    if ylim is not None: ax.set_ylim(ylim[0], ylim[1])

    for xb in x_bins[1:-1]: ax.axvline(x=float(xb), linestyle='--', linewidth=1)
    for yb in y_bins[1:-1]: ax.axhline(y=float(yb), linestyle='--', linewidth=1)

    for i in range(n_regions):
        for j in range(n_regions):
            x_c, y_c = (x_bins[i] + x_bins[i + 1]) / 2, (y_bins[j] + y_bins[j + 1]) / 2
            ax.text(x_c, y_c, f"{percentages[i, j]:.1f}%", ha='center', va='center', fontsize=10, fontweight='bold', color='black')

    fig.tight_layout()

def plot_vs_coverage_smooth(df: pd.DataFrame, x_var: str, y_var: str, x_lim=None, y_lim=None, title= "", split_plot_type="calibration"):
    if x_var not in df.columns or y_var not in df.columns: 
        print(f"[WARN] Missing coordinate columns {x_var} or {y_var}.")
        return

    fig, ax = plt.subplots(figsize=(12, 10))
    
    # 1. Split by Task Type
    if split_plot_type == "task_type":
        if 'task_type' not in df.columns: return
        test_data = df[df['task_type'] == 'test']
        if not test_data.empty: 
            sns.kdeplot(data=test_data, x=x_var, y=y_var, fill=True, color='orange', alpha=0.4, ax=ax, thresh=0.05)
        train_data = df[df['task_type'] == 'calibration']
        if not train_data.empty: 
            sns.kdeplot(data=train_data, x=x_var, y=y_var, fill=True, color='blue', alpha=0.6, ax=ax, thresh=0.05)
        ax.legend(handles=[Patch(facecolor='orange', alpha=0.4, label='16-point'), Patch(facecolor='blue', alpha=0.6, label='9-point')], loc='upper right', fontsize=30)
        
    # 2. Split by Session (SMALL MULTIPLES GRID FIX)
    elif split_plot_type == "task":
        if 'id_task' not in df.columns: 
            return
        
        df_temp = df.copy()
        df_temp['id_task'] = df_temp['id_task'].astype(str)
        reversed_tasks = sorted(df_temp['id_task'].unique().tolist(), reverse=True)
        dynamic_palette = sns.color_palette("husl", len(reversed_tasks))
        
        # Close the single axis figure created at the start to avoid empty plots
        plt.close(fig) 
        
        # Create a grid of subplots (4 columns wide)
        g = sns.displot(
            data=df_temp, x=x_var, y=y_var, col='id_task', col_wrap=4,
            col_order=reversed_tasks, kind="kde", fill=True, 
            alpha=0.6, thresh=0.05, hue='id_task', palette=dynamic_palette,
            height=3, aspect=1.2, legend=False
        )
        
        g.fig.suptitle(f"{y_var} vs {x_var} - Task Breakdown", fontsize=16, y=1.02)
        g.set_axis_labels(x_var, y_var)
        
        for ax_in_grid in g.axes.flatten():
            ax_in_grid.grid(True, linestyle='--', alpha=0.3)
            if x_lim: ax_in_grid.set_xlim(x_lim[0], x_lim[1])
            if y_lim: ax_in_grid.set_ylim(y_lim[0], y_lim[1])
            
        return # Exit early so we don't apply the single-axis formatting below
        
    # 3. Split Calibration Only
    elif split_plot_type == "calibration":
        if 'task_type' not in df.columns: return
        calib_data = df[df['task_type'] == 'calibration']
        if not calib_data.empty:
            sns.kdeplot(data=calib_data, x=x_var, y=y_var, hue='id_session', fill=True, alpha=0.4, ax=ax, thresh=0.05, palette='tab10', common_norm=False, legend=False)

    # Formatting (for single axis plots only)
    ax.set_xlabel(x_var, fontsize=32)
    ax.set_ylabel(y_var, fontsize=32)
    ax.tick_params(axis='both', which='major', labelsize=23)
    ax.grid(True, linestyle='--', alpha=0.3)
    fig.tight_layout()

def plot_hpe_histograms(df: pd.DataFrame, xlims_by_suffix=None, id_user=None):
    hpe_vars = ['hpe_r_x', 'hpe_r_y', 'hpe_r_z', 'hpe_t_x', 'hpe_t_y', 'hpe_t_z']
    if 'task_type' not in df.columns: return
    df_aux = df[df['id_user'] == str(id_user)] if id_user is not None and str(id_user) in df['id_user'].astype(str).values else df

    fig, axes = plt.subplots(2, 3, figsize=(18, 10), constrained_layout=True)
    for i, var in enumerate(hpe_vars):
        ax = axes.flatten()[i]
        if var in df_aux.columns:
            data = pd.to_numeric(df_aux[var], errors='coerce')
            ax.hist(data[df_aux['task_type'] == 'calibration'].dropna(), bins=30, alpha=0.5, label='Calibration', color='blue', density=True)
            ax.hist(data[df_aux['task_type'] == 'test'].dropna(), bins=30, alpha=0.5, label='Test', color='orange', density=True)
            
            xlim = next((lim for suf, lim in (xlims_by_suffix or {}).items() if var.endswith(suf)), None)
            if xlim: ax.set_xlim(xlim[0], xlim[1])
            ax.set_title(f"Histogram of {var}"); ax.legend()
    fig.suptitle("HPE Distribution: Calibration vs Test", fontsize=16)

def save_all_figures(export_dir: str):
    import re
    os.makedirs(export_dir, exist_ok=True)
    for i in plt.get_fignums():
        fig = plt.figure(i)
        title = fig._suptitle.get_text() if fig._suptitle else (fig.get_axes()[0].get_title() if fig.get_axes() else f"figure_{i}")
        clean_title = re.sub(r'[-\s]+', '_', re.sub(r'[^\w\s-]', '', str(title)).strip().lower())
        filepath = os.path.join(export_dir, f"{clean_title}.png")
        fig.savefig(filepath, dpi=150)

def _pandas_manipulation(df_local):
    """
    Maps the new flattened JSON columns to the legacy names expected by the plots.
    Converts 9-point/16-point into calibration/test.
    """
    rename_map = {
        'user_id': 'id_user',
        'session_id': 'id_session',
        'task_id': 'id_task',
        'hpe_r_rad_x': 'hpe_r_x',
        'hpe_r_rad_y': 'hpe_r_y',
        'hpe_r_rad_z': 'hpe_r_z',
        'hpe_t_mm_x': 'hpe_t_x',
        'hpe_t_mm_y': 'hpe_t_y',
        'hpe_t_mm_z': 'hpe_t_z',
    }
    df_local = df_local.rename(columns=rename_map)
    
    if 'task_type' in df_local.columns:
        df_local['task_type'] = df_local['task_type'].replace({
            '9-point': 'calibration',
            '16-point': 'test'
        })
    return df_local


