def plot_vs_coverage_smooth(df: pd.DataFrame, x_var: str, y_var: str, x_lim=None, y_lim=None, split_plot_type="calibration"):
    
    # Safety check for coordinates
    if x_var not in df.columns or y_var not in df.columns: 
        print(f"[WARN] Missing coordinate columns {x_var} or {y_var}.")
        return

    fig, ax = plt.subplots(figsize=(12, 10))
    
    # ---------------------------------------------------------
    # 1. Split by Task Type (Test vs Calibration)
    # ---------------------------------------------------------
    if split_plot_type == "task_type":
        if 'task_type' not in df.columns: return
        
        test_data = df[df['task_type'] == 'test']
        if not test_data.empty: 
            sns.kdeplot(data=test_data, x=x_var, y=y_var, fill=True, color='orange', alpha=0.4, ax=ax, thresh=0.05)
    
        train_data = df[df['task_type'] == 'calibration']
        if not train_data.empty: 
            sns.kdeplot(data=train_data, x=x_var, y=y_var, fill=True, color='blue', alpha=0.6, ax=ax, thresh=0.05)
            
        ax.legend(handles=[
            Patch(facecolor='orange', alpha=0.4, label='16-point (Test)'), 
            Patch(facecolor='blue', alpha=0.6, label='9-point (Train)')
        ], loc='upper right', fontsize=20)
        
    # ---------------------------------------------------------
    # 2. Split by Session (Unique color for every session)
    # ---------------------------------------------------------
    elif split_plot_type == "task":
        if 'id_task' not in df.columns: 
            return
        
        # 1. Force to string so Seaborn treats it as categorical
        df_temp = df.copy()
        df_temp['id_task'] = df_temp['id_task'].astype(str)
        
        # 2. Get unique tasks, sort them, and REVERSE the order
        # e.g., ['01', '02', '03'] becomes ['03', '02', '01']
        reversed_tasks = sorted(df_temp['id_task'].unique().tolist(), reverse=True)
        
        # 3. Create a dynamic palette based on the number of unique tasks
        dynamic_palette = sns.color_palette("husl", len(reversed_tasks))
        
        # 4. Plot using hue_order to force the inverted sequence
        sns.kdeplot(
            data=df_temp, x=x_var, y=y_var, hue='id_task', 
            hue_order=reversed_tasks,  # <--- THIS FORCES THE REVERSE ORDER
            fill=True, alpha=0.4, ax=ax, thresh=0.05, 
            palette=dynamic_palette, common_norm=False
        )
        
        # 5. Safely update legend sizing
        if ax.get_legend() is not None:
            plt.setp(ax.get_legend().get_texts(), fontsize='16') 
            plt.setp(ax.get_legend().get_title(), fontsize='18')

    # ---------------------------------------------------------
    # 3. Split Calibration Only (Unique color for calibration tasks)
    # ---------------------------------------------------------
    elif split_plot_type == "calibration":
        if 'task_type' not in df.columns: return
        
        # Filter down to only calibration tasks
        calib_data = df[df['task_type'] == 'calibration']
        
        if not calib_data.empty:
            # You can change 'session' to 'task' below if you specifically 
            # want to color code by the task ID instead of the session ID
            group_by_col = 'id_session'
            
            sns.kdeplot(
                data=calib_data, x=x_var, y=y_var, hue=group_by_col, 
                fill=True, alpha=0.4, ax=ax, thresh=0.05, 
                palette='tab10', common_norm=False
            )
            plt.setp(ax.get_legend().get_texts(), fontsize='16') 
            plt.setp(ax.get_legend().get_title(), fontsize='18')

    # ---------------------------------------------------------
    # Formatting
    # ---------------------------------------------------------
    ax.set_xlabel(x_var, fontsize=22)
    ax.set_ylabel(y_var, fontsize=22)
    ax.tick_params(axis='both', which='major', labelsize=18)
    ax.set_title(f"{y_var} vs {x_var} - {split_plot_type.replace('_', ' ').title()}", fontsize=24)
    if x_lim: ax.set_xlim(x_lim[0], x_lim[1])
    if y_lim: ax.set_ylim(y_lim[0], y_lim[1])
    
    ax.grid(True, linestyle='--', alpha=0.3)
    fig.tight_layout()

def plot_hpe_histograms(df: pd.DataFrame, xlims_by_suffix=None, id_user=None):
    hpe_vars = ['hpe_r_x', 'hpe_r_y', 'hpe_r_z', 'hpe_t_x', 'hpe_t_y', 'hpe_t_z']
    if 'task_type' not in df.columns: return
    df_aux = df[df['id_user'] == str(id_user)] if id_user is not None and str(id_user) in df['id_user'].astype(str).values else df

    fig, axes = plt.subplots(2, 3, figsize=(18, 10), constrained_layout=True)
    for i, var in enumerate(hpe_vars):
        ax = axes.flatten()[i]
        if var in df_aux.columns:
            data = pd.to_numeric(df_aux[var], errors='coerce')
            ax.hist(data[df_aux['task_type'] == 'calibration'].dropna(), bins=30, alpha=0.5, label='Calibration', color='blue', density=True)
            ax.hist(data[df_aux['task_type'] == 'test'].dropna(), bins=30, alpha=0.5, label='Test', color='orange', density=True)
            
            xlim = next((lim for suf, lim in (xlims_by_suffix or {}).items() if var.endswith(suf)), None)
            if xlim: ax.set_xlim(xlim[0], xlim[1])
            ax.set_title(f"Histogram of {var}"); ax.legend()
    fig.suptitle("HPE Distribution: Calibration vs Test", fontsize=16)

def save_all_figures(export_dir: str):
    import re
    os.makedirs(export_dir, exist_ok=True)
    for i in plt.get_fignums():
        fig = plt.figure(i)
        title = fig._suptitle.get_text() if fig._suptitle else (fig.get_axes()[0].get_title() if fig.get_axes() else f"figure_{i}")
        filepath = os.path.join(export_dir, f"{re.sub(r'[-\s]+', '_', re.sub(r'[^\w\s-]', '', title).strip().lower())}.png")
        fig.savefig(filepath, dpi=150)

def _pandas_manipulation(df_local):
    """
    Maps flattened JSON columns to the legacy names expected by the analyzer.
    Converts 9-point/16-point into calibration/test.
    """
    
    # Determine Task Type
    if 'task_type' in df_local.columns:
        df_local['task_type'] = df_local['task_type'].replace({
            '9-point': 'calibration',
            '16-point': 'test'
        })
    return df_local


