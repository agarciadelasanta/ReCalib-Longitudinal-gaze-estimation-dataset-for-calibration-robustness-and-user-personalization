def load_gaze_analysis_params(params_path: str) -> dict:
    if not params_path:
        return {}
    if not os.path.isfile(params_path):
        print(f"[WARN] params yaml not found: {params_path}")
        return {}
    with open(params_path, "r", encoding="utf-8") as f:
        return yaml.safe_load(f) or {}

