def calc_percentage_outliers_removed(df_full, x_value, y_value, n_regions, outlier_q=0.01, xlims=None, ylims=None):
    x = pd.to_numeric(df_full[x_value], errors="coerce")
    y = pd.to_numeric(df_full[y_value], errors="coerce")
    valid = x.notna() & y.notna()

    if valid.sum() == 0:
        raise ValueError(f"No hay datos válidos en {x_value}, {y_value}")

    x_v = x[valid].to_numpy(dtype=float)
    y_v = y[valid].to_numpy(dtype=float)

    x_low, x_high = np.quantile(x_v, [outlier_q, 1.0 - outlier_q])
    y_low, y_high = np.quantile(y_v, [outlier_q, 1.0 - outlier_q])

    inlier_mask = (valid & (x >= x_low) & (x <= x_high) & (y >= y_low) & (y <= y_high))

    df_filtered = df_full.loc[inlier_mask].copy()
    xf = pd.to_numeric(df_filtered[x_value], errors="coerce").to_numpy(dtype=float)
    yf = pd.to_numeric(df_filtered[y_value], errors="coerce").to_numpy(dtype=float)

    x_min, x_max = float(np.min(xf)) if xlims is None else float(xlims[0]), float(np.max(xf)) if xlims is None else float(xlims[1])
    y_min, y_max = float(np.min(yf)) if ylims is None else float(ylims[0]), float(np.max(yf)) if ylims is None else float(ylims[1])

    if np.isclose(x_min, x_max): x_min -= 1e-6; x_max += 1e-6
    if np.isclose(y_min, y_max): y_min -= 1e-6; y_max += 1e-6

    x_bins = np.linspace(x_min, x_max, n_regions + 1)
    y_bins = np.linspace(y_min, y_max, n_regions + 1)

    x_idx = np.clip(np.digitize(xf, x_bins) - 1, 0, n_regions - 1)
    y_idx = np.clip(np.digitize(yf, y_bins) - 1, 0, n_regions - 1)

    region_counts = np.zeros((n_regions, n_regions), dtype=int)
    for xi, yi in zip(x_idx, y_idx):
        region_counts[int(xi), int(yi)] += 1

    total = int(region_counts.sum())
    percentages = (region_counts / total * 100.0) if total > 0 else region_counts.astype(float)

    region_weights = {rid: round(100.0 / float(perc), 4) if perc > 0 else 1.0 for rid, perc in enumerate(percentages.flatten())}

    return region_counts, percentages, x_bins, y_bins, region_weights, df_filtered

