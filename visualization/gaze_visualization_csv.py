def analyze(args):
    params = load_gaze_analysis_params(args.params_yaml)

    x_axis_hpe_r_lim = params.get("x_axis_hpe_r_lim", [-0.3, 0.3])
    y_axis_hpe_r_lim = params.get("y_axis_hpe_r_lim", [-0.3, 0.3])
    z_axis_hpe_r_lim = params.get("z_axis_hpe_r_lim", [-0.3, 0.3])

    x_axis_hpe_t_lim = params.get("x_axis_hpe_t_lim", [-500, 500])
    y_axis_hpe_t_lim = params.get("y_axis_hpe_t_lim", [-500, 500])
    z_axis_hpe_t_lim = params.get("z_axis_hpe_t_lim", [0, 1000])

    x_axis_ge_lim = params.get("x_axis_ge_lim", [-1.0, 1.0])
    y_axis_ge_lim = params.get("y_axis_ge_lim", [-1.0, 1.0])
    
    x_axis_pog_lim = params.get("x_axis_pog_lim", [-200, 200])
    y_axis_pog_lim = params.get("y_axis_pog_lim", [-200, 200])
    
    x_axis_pog_norm_lim = params.get("x_axis_pog_norm_lim", [-1.0, 1.0])
    y_axis_pog_norm_lim = params.get("y_axis_pog_norm_lim", [-1.0, 1.0])

    screen_h=params.get("screen_h", 31.2)
    screen_w=params.get("screen_w", 51.4)
    cam_y=params.get("cam_y", 10)
    
    id_user, id_session, id_task = args.id_user, args.id_session, args.task
                
    df = pd.read_csv(args.csv, sep=args.sep)
    print(f"Loaded CSV: {len(df)} rows, {len(df.columns)} columns")
    
    # 1. APPLY MAPPING FIRST
    df = _pandas_manipulation(df)
    
    # 2. DISCARD FLAGGED SAMPLES
    if 'discard_info' in df.columns:
        df = df[df['discard_info'].isna()]
    elif 'discard_info_discarded' in df.columns:
        df = df[df['discard_info_discarded'].isna() | (df['discard_info_discarded'] == False)]
        
    print(f"After sample discard CSV: {len(df)} rows, {len(df.columns)} columns")
    
    # 3. FILTER BY USER / SESSION / TASK
    if id_user is not None and 'id_user' in df.columns:
        df = df[pd.to_numeric(df['id_user'], errors='coerce') == int(id_user)]
        print(f"After user filtration CSV: {len(df)} rows")
        
        if id_session is not None and 'id_session' in df.columns:
            df = df[pd.to_numeric(df['id_session'], errors='coerce') == int(id_session)]
            print(f"After session filtration CSV: {len(df)} rows")
            
            if id_task is not None and 'id_task' in df.columns:
                df = df[pd.to_numeric(df['id_task'], errors='coerce') == int(id_task)]
                print(f"After task filtration CSV: {len(df)} rows")
                
    if len(df) == 0:
        print("[WARN] No data left after filtering! Exiting.")
        return
        
    df = _pandas_manipulation(df)
    
    gaze_cols = ['gaze_vector_x', 'gaze_vector_y', 'gaze_vector_z']
    gaze_vectors = df[gaze_cols].apply(pd.to_numeric, errors="coerce").values

    # Intersect norm
    pog_norm = np.array([intersect_gaze(g) for g in gaze_vectors])
    df['pog_norm_x'] = pog_norm[:, 0]
    df['pog_norm_y'] = pog_norm[:, 1]

    # Intersect plane
    if all(c in df.columns for c in ['hpe_t_x', 'hpe_t_y', 'hpe_t_z']):
        origins = df[['gaze_origin_mm_x', 'gaze_origin_mm_y', 'gaze_origin_mm_z']].apply(pd.to_numeric, errors="coerce").values 
        pog = np.array([intersect_gaze(d, o) for d, o in zip(gaze_vectors, origins)])
        df['pog_intersect_x'] = pog[:, 0]
        df['pog_intersect_y'] = pog[:, 1]

    n_regions = args.n_regions

    region_counts, percentages, x_bins, y_bins, region_weights, df_pog_inliers = calc_percentage_outliers_removed(
        df, 'pog_intersect_x', 'pog_intersect_y', n_regions=n_regions, outlier_q=args.outlier_q,
    )

    region_counts_norm, percentages_norm, x_bins_norm, y_bins_norm, region_weights_norm, df_pog_norm_inliers = calc_percentage_outliers_removed(
        df, 'pog_norm_x', 'pog_norm_y', n_regions=n_regions, outlier_q=args.outlier_q,
    )
    
    if not args.no_plots:
        plot_scatter_with_grid(df, x_bins_norm, y_bins_norm, 'pog_norm_x', 'pog_norm_y', n_regions, percentages_norm, title="Normalized POG mm", show_screen=False, xlim=x_axis_pog_norm_lim, ylim=y_axis_pog_norm_lim)
        plot_scatter_with_grid(df, x_bins, y_bins, 'pog_intersect_x', 'pog_intersect_y', n_regions, percentages, title="Calculated POG mm ", show_screen=True, xlim=x_axis_pog_lim, ylim=y_axis_pog_lim, screen_h=screen_h, screen_w=screen_w, cam_y=cam_y)
        plot_scatter_with_grid(df, x_bins, y_bins, 'gaze_pog_mm_x', 'gaze_pog_mm_y', n_regions, percentages, title="Labelled POG mm", show_screen=True, xlim=x_axis_pog_lim, ylim=y_axis_pog_lim, screen_h=screen_h, screen_w=screen_w, cam_y=cam_y)
        
        xlims_hpe = {"_t_x": x_axis_hpe_t_lim, "_t_y": y_axis_hpe_t_lim, "_t_z": z_axis_hpe_t_lim, "_r_x": x_axis_hpe_r_lim, "_r_y": y_axis_hpe_r_lim, "_r_z": z_axis_hpe_r_lim}
        plot_hpe_histograms(df, xlims_by_suffix=xlims_hpe)
        
        if id_user is not None:
            if id_session is not None:
                plot_vs_coverage_smooth(df, "hpe_t_x", "hpe_t_y", x_lim=x_axis_hpe_t_lim, y_lim=y_axis_hpe_t_lim, split_plot_type="task", title="Task Coverage - HPE_t X vs Y")
                plot_vs_coverage_smooth(df, "hpe_t_x", "hpe_t_z", x_lim=x_axis_hpe_t_lim, y_lim=z_axis_hpe_t_lim, split_plot_type="task", title="Task Coverage - HPE_t X vs Z")
                plot_vs_coverage_smooth(df, "hpe_r_x", "hpe_r_y", x_lim=x_axis_hpe_r_lim, y_lim=y_axis_hpe_r_lim, split_plot_type="task", title="Task Coverage - HPE_r X vs Y")
                plot_vs_coverage_smooth(df, "hpe_r_x", "hpe_r_z", x_lim=x_axis_hpe_r_lim, y_lim=z_axis_hpe_r_lim, split_plot_type="task", title="Task Coverage - HPE_r X vs Z")
                plot_vs_coverage_smooth(df, "gaze_vector_x", "gaze_vector_y", x_lim=x_axis_ge_lim, y_lim=y_axis_ge_lim, split_plot_type="task", title="Task Coverage - Gaze Vector X vs Y")
            else:
                plot_vs_coverage_smooth(df, "hpe_t_x", "hpe_t_y", x_lim=x_axis_hpe_t_lim, y_lim=y_axis_hpe_t_lim, split_plot_type="calibration", title="Calib. Session Coverage - HPE_t X vs Y")
                plot_vs_coverage_smooth(df, "hpe_t_x", "hpe_t_z", x_lim=x_axis_hpe_t_lim, y_lim=z_axis_hpe_t_lim, split_plot_type="calibration", title="Calib. Session Coverage - HPE_t X vs Z")
                plot_vs_coverage_smooth(df, "hpe_r_x", "hpe_r_y", x_lim=x_axis_hpe_r_lim, y_lim=y_axis_hpe_r_lim, split_plot_type="calibration", title="Calib. Session Coverage - HPE_r X vs Y")
                plot_vs_coverage_smooth(df, "hpe_r_x", "hpe_r_z", x_lim=x_axis_hpe_r_lim, y_lim=z_axis_hpe_r_lim, split_plot_type="calibration", title="Calib. Session Coverage - HPE_r X vs Z")
                plot_vs_coverage_smooth(df, "gaze_vector_x", "gaze_vector_y", x_lim=x_axis_ge_lim, y_lim=y_axis_ge_lim, split_plot_type="calibration", title="Calib. Session Coverage - Gaze Vector X vs Y")

            plot_vs_coverage_smooth(df, "hpe_t_x", "hpe_t_y", x_lim=x_axis_hpe_t_lim, y_lim=y_axis_hpe_t_lim, split_plot_type="task_type", title="Overall Coverage - HPE_t X vs Y")
            plot_vs_coverage_smooth(df, "hpe_t_x", "hpe_t_z", x_lim=x_axis_hpe_t_lim, y_lim=z_axis_hpe_t_lim, split_plot_type="task_type", title="Overall Coverage - HPE_t X vs Z")
            plot_vs_coverage_smooth(df, "hpe_r_x", "hpe_r_y", x_lim=x_axis_hpe_r_lim, y_lim=y_axis_hpe_r_lim, split_plot_type="task_type", title="Overall Coverage - HPE_r X vs Y")
            plot_vs_coverage_smooth(df, "hpe_r_x", "hpe_r_z", x_lim=x_axis_hpe_r_lim, y_lim=z_axis_hpe_r_lim, split_plot_type="task_type", title="Overall Coverage - HPE_r X vs Z")
            plot_vs_coverage_smooth(df, "gaze_vector_x", "gaze_vector_y", x_lim=x_axis_ge_lim, y_lim=y_axis_ge_lim, split_plot_type="task_type", title="Overall Coverage - Gaze Vector X vs Y")
            plot_vs_coverage_smooth(df, "pog_norm_x", "pog_norm_y", x_lim=x_axis_ge_lim, y_lim=y_axis_ge_lim, split_plot_type="task_type", title="Overall Coverage - POG Normalized X vs Y")

    if args.export_dir:
        os.makedirs(args.export_dir, exist_ok=True)
        save_all_figures(os.path.join(args.export_dir, "plots"))
        df.to_csv(os.path.join(args.export_dir, "augmented.csv"), index=False, sep=args.sep)

    plt.show(block=True)

