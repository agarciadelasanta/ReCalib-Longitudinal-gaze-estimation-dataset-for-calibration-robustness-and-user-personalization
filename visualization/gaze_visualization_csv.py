def plot_scatter_with_grid(df_plot, x_bins, y_bins, x_value, y_value, n_regions, percentages, title, xlim=None, ylim=None, show_screen=False, screen_w=260, screen_h=170, cam_y=10):
    x = pd.to_numeric(df_plot[x_value], errors="coerce")
    y = pd.to_numeric(df_plot[y_value], errors="coerce")
    
    fig, ax = plt.subplots(figsize=(8, 6))

    # Draw Screen boundary
    if show_screen:
        # Changed edgecolor to black so it doesn't blend with orange 'test' dots
        rect = Rectangle((-screen_w / 2.0, cam_y), screen_w, screen_h, edgecolor='black', facecolor='none', linewidth=2, linestyle='-')
        ax.add_patch(rect)
        
    # Check if we have task_type for coloring
    if 'task_type' in df_plot.columns:
        # Dictionary to keep colors consistent with your smooth coverage plots
        color_map = {
            'test': 'orange', 
            'calibration': 'blue', 
        }
        
        unique_tasks = df_plot['task_type'].dropna().unique()
        
        # 1. Explicitly force 'test' to plot first (background), then 'calibration' (foreground)
        ordered_tasks = [t for t in ['test', 'calibration'] if t in unique_tasks]
        
        # 2. Append any other unexpected task types (if any exist) to the end
        ordered_tasks += [t for t in unique_tasks if t not in ['test', 'calibration']]
        
        # 3. Loop over the strictly ordered list
        for t_type in ordered_tasks:
            # Mask for valid coordinates AND specific task type
            m = (df_plot['task_type'] == t_type) & x.notna() & y.notna()
            
            if m.sum() > 0:
                c = color_map.get(t_type, 'gray') # fallback to gray if unknown
                # Matplotlib naturally layers items in the order they are plotted
                ax.scatter(x[m], y[m], alpha=0.5, s=8, color=c, label=t_type.capitalize())
                
        ax.legend(loc='upper right')
    else:
        # Fallback if task_type is missing
        m = x.notna() & y.notna()
        ax.scatter(x[m], y[m], alpha=0.5, s=8, label="All Data")
        ax.legend(loc='upper right')

    ax.set_xlabel(x_value)
    ax.set_ylabel(y_value)
    ax.set_title(title)

    if xlim is not None: ax.set_xlim(xlim[0], xlim[1])
    if ylim is not None: ax.set_ylim(ylim[0], ylim[1])

    # Draw Grid lines based on calculated bins
    for xb in x_bins[1:-1]: ax.axvline(x=float(xb), linestyle='--', linewidth=1)
    for yb in y_bins[1:-1]: ax.axhline(y=float(yb), linestyle='--', linewidth=1)

    # Add Percent labels inside the grid
    for i in range(n_regions):
        for j in range(n_regions):
            x_c, y_c = (x_bins[i] + x_bins[i + 1]) / 2, (y_bins[j] + y_bins[j + 1]) / 2
            # Use black text for visibility
            ax.text(x_c, y_c, f"{percentages[i, j]:.1f}%", ha='center', va='center', fontsize=10, fontweight='bold', color='black')

    fig.tight_layout()


